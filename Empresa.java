public class Empresa {

    private String nombre;
    private String tematica;
    private String ubicacion;
    private int nit;
    private int Nempleados;
    private int sedes;

    public Empresa() {
    }

    public Empresa(String nombre, String tematica, String ubicacion, int nit, int Nempleados, int sedes) {
        this.nombre = nombre;
        this.tematica = tematica;
        this.ubicacion = ubicacion;
        this.nit = nit;
        this.Nempleados = Nempleados;
        this.sedes = sedes;
    }
    
    

}
