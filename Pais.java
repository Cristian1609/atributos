public class Pais {
    
    private String nombre;
    private String lenguaje;
    private double longitud;
    private int Nciudades;
    private int NDepartamentos;
    private int Npoblacion;
    private String ubicacion;

    public Pais() {
    }

    public Pais(String nombre, String lenguaje, double longitud, int Nciudades, int NDepartamentos, int Npoblacion, String ubicacion) {
        this.nombre = nombre;
        this.lenguaje = lenguaje;
        this.longitud = longitud;
        this.Nciudades = Nciudades;
        this.NDepartamentos = NDepartamentos;
        this.Npoblacion = Npoblacion;
        this.ubicacion = ubicacion;
    }
  
    
}
